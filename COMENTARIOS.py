def extraer_comentarios_youtube(video_id, api_key, max_resultados=200):
    """
    Extrae comentarios reales de un video de YouTube.

    Args:
        video_id: ID del video (de la URL)
        api_key: Tu clave de API de YouTube
        max_resultados: Número máximo de comentarios

    Returns:
        DataFrame con los comentarios extraídos
    """
    print(f"🌐 Conectando con YouTube API - Video ID: {video_id}")

    # Crear cliente de la API
    youtube = googleapiclient.discovery.build(
        "youtube", "v3", developerKey=api_key)

    comentarios = []
    next_page_token = None

    try:
        while len(comentarios) < max_resultados:
            # Solicitar comentarios a YouTube
            request = youtube.commentThreads().list(
                part="snippet",
                videoId=video_id,
                maxResults=min(100, max_resultados - len(comentarios)),
                pageToken=next_page_token,
                textFormat="plainText"
            )
            response = request.execute()

            # Procesar cada comentario
            for item in response['items']:
                comentario = item['snippet']['topLevelComment']['snippet']['textDisplay']
                fecha = item['snippet']['topLevelComment']['snippet']['publishedAt']
                likes = item['snippet']['topLevelComment']['snippet']['likeCount']

                comentarios.append({
                    'comentario_original': comentario,
                    'fecha': fecha,
                    'likes': likes
                })

            # Verificar si hay más páginas de comentarios
            next_page_token = response.get('nextPageToken')
            if not next_page_token:
                break

            time.sleep(0.1)  # Pequeña pausa para respetar límites de API

        print(f"✅ Extraídos {len(comentarios)} comentarios reales")
        return pd.DataFrame(comentarios)

    except Exception as e:
        print(f"❌ Error en API: {e}")
        print("\n📌 Usando datos de ejemplo para continuar el análisis...")
        return crear_datos_ejemplo()

