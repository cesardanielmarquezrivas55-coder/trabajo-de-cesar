# ============================================
# Eliminamos elementos que no aportan al análisis:
# - URLs, menciones (@usuario), hashtags (#)
# - Caracteres especiales, números
# - Emojis y símbolos
# - Espacios extras

def limpiar_texto(texto):
    """
    Limpia el texto de cada comentario:
    - Convierte a minúsculas
    - Elimina URLs, menciones, hashtags
    - Elimina caracteres especiales y números
    - Normaliza espacios
    """
    if not isinstance(texto, str):
        return ""

    # Convertir a minúsculas
    texto = texto.lower()

    # Eliminar URLs (http, https, www)
    texto = re.sub(r'http\S+|www\S+|https\S+', '', texto)

    # Eliminar menciones (@usuario)
    texto = re.sub(r'@\w+', '', texto)

    # Eliminar hashtags pero mantener la palabra (quitar #)
    texto = re.sub(r'#(\w+)', r'\1', texto)

    # Eliminar caracteres especiales y números (solo letras y espacios)
    texto = re.sub(r'[^a-záéíóúüñ\s]', '', texto)

    # Eliminar espacios extras
    texto = re.sub(r'\s+', ' ', texto).strip()

    return texto

