def clasificar_sentimiento(tokens):
    """
    Clasifica un comentario como positivo, negativo o neutral
    basado en la comparación de palabras positivas vs negativas.
    """
    if not tokens:
        return 'neutral'

    # Contar ocurrencias de palabras positivas y negativas
    pos_count = sum(1 for token in tokens if token in palabras_positivas)
    neg_count = sum(1 for token in tokens if token in palabras_negativas)

    if pos_count > neg_count:
        return 'positivo'
    elif neg_count > pos_count:
        return 'negativo'
    else:
        return 'neutral'

