def generar_nube_palabras(tokens_list, titulo, color_fondo='white', colormap='viridis'):
    """
    Genera una nube de palabras a partir de una lista de tokens.

    Args:
        tokens_list: Lista de listas de tokens
        titulo: Título para la gráfica
        color_fondo: Color de fondo de la nube
        colormap: Esquema de colores
    """
    # Unir todos los tokens en un solo texto
    texto_completo = ' '.join([' '.join(tokens) for tokens in tokens_list if tokens])

    if not texto_completo:
        print(f"⚠️ No hay suficientes palabras para: {titulo}")
        return

    # Crear la nube de palabras
    wordcloud = WordCloud(
        width=800,
        height=400,
        background_color=color_fondo,
        colormap=colormap,
        max_words=100,
        min_font_size=10,
        max_font_size=100,
        random_state=42
    ).generate(texto_completo)

    # Visualizar
    plt.figure(figsize=(12, 6))
    plt.imshow(wordcloud, interpolation='bilinear')
    plt.title(titulo, fontsize=16, fontweight='bold', pad=20)
    plt.axis('off')
    plt.tight_layout()
    plt.show()

